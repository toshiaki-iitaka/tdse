C
C STABILITY CHECK OF D4 METHOD (4TH ORDER DIFFERENCIG SCHEME)
C USE IMSL
C
C (C) TOSHIAKI IITAKA 1994
C REFERENCE: T.Iitaka, Phys. Rev. E49 (1994) 4684.
C
      PROGRAM ST4
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      PARAMETER(NDEG=4)
      COMPLEX*16  COEFF(NDEG+1),ROOT(NDEG),IU
      OPEN(20,FILE='st4.dat')
      DO 10 A=0.0, 1.0, 0.01
      IU=(0.0,1.0)
      COEFF(1)=-1
      COEFF(2)=+8./3.*IU*A
      COEFF(3)=-4./3.*IU*A
      COEFF(4)=+8./3.*IU*A
      COEFF(5)=1
      CALL DZPOCC(NDEG,COEFF,ROOT)
      DO 20 I=1,NDEG
      WRITE(*,100) A,REAL(ROOT(I)),IMAG(ROOT(I)),ABS(ROOT(I))-1
   20 CONTINUE
      WRITE(20,100) A,(ABS(ROOT(I)),I=1,NDEG)
  100 FORMAT(1H ,10E15.7)
   10 CONTINUE
      CLOSE(20)
      STOP
      END
